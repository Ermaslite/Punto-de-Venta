   private void cargarDatos() {
    String[] columnas = { "ID", "CODIGO PRODUCTO", "DESCRIPCION", "CODIGO DE BARRA", "MARCA", "RUBRO", "PROVEEDOR", "PRECIO FINAL" };
    modeloTabla = new DefaultTableModel(null, columnas); 
    jTable1.setModel(modeloTabla);
    
    List<String[]> productos = backend.cargarProductos();
    for (String[] producto : productos) { 
        modeloTabla.addRow(producto); 
    }
}
   
   public void actualizarTabla() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        List<String[]> empleados = backend.cargarEmpleados();
        for (String[] empleado : empleados) {
            model.addRow(empleado);
        }
        model.fireTableDataChanged();
    }
