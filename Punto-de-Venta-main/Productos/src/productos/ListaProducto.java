    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        String productoId = modeloTabla.getValueAt(selectedRow, 0).toString();
        nProducto productoEWindow = new nProducto(backend, productoId, this);
        productoEWindow.setVisible(true);
        dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona un producto para editar.");
    }

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        String productoId = modeloTabla.getValueAt(selectedRow, 0).toString();
        int confirm = JOptionPane.showConfirmDialog(this, "¿Estás seguro de que deseas eliminar este producto?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            boolean resultado = backend.eliminarProducto(productoId);
            if (resultado) {
                JOptionPane.showMessageDialog(this, "Producto eliminado con éxito!");
                cargarDatos(); // Actualiza la tabla después de eliminar el producto
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar el producto. Inténtalo de nuevo.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona un producto para eliminar.");
    }

    }//GEN-LAST:event_jButton4ActionPerformed

